#include <iostream>

int main() {
    // Инициализация двумерных массивов для каждого типа предметов
    // Каждый массив имеет размерность 2x6, где 2 — количество рядов, а 6 — количество мест в ряду
    
    // Количество вилок, ножей и ложек для каждого места
    int utensils[2][6] = {
        {3, 3, 3, 3, 3, 3},  // Первый ряд
        {3, 3, 3, 3, 4, 4}   // Второй ряд, VIP-персоны имеют дополнительную ложку
    };

    // Количество тарелок для каждого места (по две на каждого человека и одна дополнительная для VIP-персон)
    int plates[2][6] = {
        {2, 2, 2, 2, 3, 3},  // Первый ряд
        {2, 2, 2, 2, 3, 3}   // Второй ряд
    };

    // Количество стульев для каждого места (по одному на каждого человека)
    int chairs[2][6] = {
        {1, 1, 1, 1, 1, 1},  // Первый ряд
        {1, 1, 1, 1, 1, 1}   // Второй ряд
    };

    // События на банкете
    // 1. Пришедшая на пятое место первого ряда дама привела с собой ребёнка, и поэтому на данное место был приставлен ещё один стул.
    chairs[0][4] += 1;

    // 2. С третьего места во втором ряду была украдена ложка.
    utensils[1][2] -= 1;

    // 3. Одна из VIP-персон поделилась своей ложкой с тем, кто остался без ложки, и стала есть суп десертной.
    utensils[1][2] += 1;  // Вернули ложку на третье место второго ряда
    utensils[1][4] -= 1;  // VIP-персона передала свою ложку

    // 4. Официант забрал у VIP-гостя одну десертную тарелку.
    plates[1][4] -= 1;

    // Вывод окончательных значений
    std::cout << "Окончательное количество предметов на банкете:\n";
    std::cout << "Утварь:\n";
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 6; j++) {
            std::cout << "Ряд " << i + 1 << ", Место " << j + 1 << ": " << utensils[i][j] << " предметов\n";
        }
    }

    std::cout << "\nТарелки:\n";
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 6; j++) {
            std::cout << "Ряд " << i + 1 << ", Место " << j + 1 << ": " << plates[i][j] << " тарелок\n";
        }
    }

    std::cout << "\nСтулья:\n";
    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 6; j++) {
            std::cout << "Ряд " << i + 1 << ", Место " << j + 1 << ": " << chairs[i][j] << " стульев\n";
        }
    }

    return 0;
}
